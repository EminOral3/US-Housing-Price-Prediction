#We decided to add the histogram of the rental prices to provide better understanding
# as Usual, we add the necessary libraries
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# we are loading our training data
df = pd.read_csv('development.csv')

# After some trials,we saw that excluding the top 1% outliers in the 'price' column provides better histogram
upper = df['price'].quantile(0.99)
filtered_prices = df[df['price'] <= upper]['price']

plt.figure(figsize=(12, 6))
sns.histplot(filtered_prices, bins=50, kde=True, color='skyblue', edgecolor='black')

#these are the labels
plt.title('Distribution of Monthly Rental Prices (Excluding Top 1% Outliers)')
plt.xlabel('Monthly Rent ($)')
plt.ylabel('Number of Listings')
plt.tight_layout()
plt.show()