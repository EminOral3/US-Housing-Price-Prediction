# We decide to use correlation heatmap in our report to show the correlation between the features in the dataset
# as usual, we import the necessary stuff
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# we are Loading our data
df = pd.read_csv('development.csv')

# we select some features
cols = ['bathrooms', 'bedrooms', 'price', 'square_feet', 'latitude', 'longitude', 'time', 'id']
corr = df[cols].corr()

plt.figure(figsize=(12, 10))
sns.heatmap(
    corr,
    annot=True,                # to Show correlation values
    fmt=".2f",                 # 2 decimal
    cmap='coolwarm',           # we decide blue to red colormap
    vmin=-1, vmax=1,           # Correlation value range
    linewidths=0,              # No grid lines
    cbar_kws={"shrink": 1.0}   # Colorbar size
)

plt.title('Correlation Heatmap of Numeric Features')
plt.tight_layout()
plt.show()