import pandas as pd  # we added it for loading, manipulating, and saving tabular data
import numpy as np  # it is for mathematical operations and numerical arrays

# all these sklearn imports are for cleaning data, turning words to numbers, splitting data, searching for best settings, and checking how good our model is
from sklearn.model_selection import train_test_split, RandomizedSearchCV, KFold
from sklearn.preprocessing import StandardScaler, OneHotEncoder
from sklearn.impute import SimpleImputer
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics import mean_absolute_error

from scipy.sparse import hstack, csr_matrix  # For combining sparse feature matrices efficiently
import lightgbm as lgb  # we use lightgbm because it's super fast(we experienced diffucultises with the speed in google colab), and its really good for lots of rows/columns

# ---------------------------------------------
# 1. Data Loading = We will load our data now
# --------------------------------------------
train_data = pd.read_csv('development.csv')
test_data = pd.read_csv('evaluation.csv')

print(f"Train: {train_data.shape}, Test: {test_data.shape}")  # we want to know the number of rows/columns in each dataset

# --------------------------------------
# 2. Data Cleaning (we think it is so cruical to obtain better results in a faster way)
# ---------------------------------------
# Wa want to prepare our raw data by handling missing values, ensuring data types are correct, and standardizing the format of each column.

def clean_data(df):
    num_cols = ['bathrooms', 'bedrooms', 'square_feet', 'fee']
    for col in num_cols:
        df[col] = pd.to_numeric(df[col], errors='coerce')
    # To Map has_photo to 1/0 before filling missing values
    df['has_photo'] = df['has_photo'].map({'Yes': 1, 'No': 0})
    df['has_photo'] = df['has_photo'].fillna(0).astype(int)
    # it'ss for the pets
    df['pets_allowed'] = df['pets_allowed'].fillna('unknown')
    # We want to Fill fee with 0 if missing
    df['fee'] = df['fee'].fillna(0)
    # to fill text columns with empty string so text vectorizers work properly
    text_cols = ['title', 'body', 'amenities']
    for col in text_cols:
        df[col] = df[col].fillna('')
    # in order to fill other categorical columns with 'unknown' if present, so that encoders work and don't treat NaN as a separate category
    for col in ['cityname', 'state', 'category', 'price_type', 'source']:
        if col in df.columns:
            df[col] = df[col].fillna('unknown')
    return df

# We are cleaning both train and test sets so that all missing values and data types are handled consistently
train_data = clean_data(train_data)
test_data = clean_data(test_data)

# ----------------------
# 3. Feature Engineering----------------------
# ----------------------
# We are doing feature enginerring because we want to create new features to help the model capture more information from the raw data.
# From our DSL Lectures/Slides, we know that it is essential for improving model performance by making important patterns more explicit.
# we had some difficulties in writing this part

def add_features(df):
    df['amenities_count'] = df['amenities'].str.split(',').apply(lambda x: len([s for s in x if s.strip() != '']))  # to count amenities
    df['location'] = df['cityname'] + ', ' + df['state']  # we want to make city and state together
    df['time'] = pd.to_datetime(df['time'])  # in order to make time usable
    df['hour_posted'] = df['time'].dt.hour  # what hour posted
    df['weekday_posted'] = df['time'].dt.weekday  # what day posted
    df['is_weekend'] = (df['weekday_posted'] >= 5).astype(int)  # to see if is it weekend
    df['title_len'] = df['title'].apply(lambda x: len(x.split()))  # words in title
    df['body_len'] = df['body'].apply(lambda x: len(x.split()))  # words in body
    df['amenities_len'] = df['amenities'].apply(lambda x: len(x.split(',')))  # amenities count again
    df['lat_bin'] = (df['latitude'] * 10).round(0)  # to group latitude
    df['lon_bin'] = (df['longitude'] * 10).round(0)  #to group longitude
    return df

# we are applying feature engineering to both train and test datasets
train_data = add_features(train_data)
test_data = add_features(test_data)

# we want to remove outliers in our target variable (price) to prevent extreme values
# and we decide to keep only listings with price within the 0.5th and 99.5th percentile range, after some trials/research
lower, upper = np.percentile(train_data['price'], [0.5, 99.5])
train_data = train_data[(train_data['price'] >= lower) & (train_data['price'] <= upper)].reset_index(drop=True)

# --------------------------------------
# 4. Text Features
# --------------------------------------

# we convert key text fields into numerical features
# we use TfidfVectorizer to transform text into a set of numbers reflecting the importance of each word.
# after some research/trials, it seemed most sensible way

# after examining the data, we picked 100 for title, 250 for body, and 50 for amenities so we get enough
#important words from each text field, without making the data too huge or slow for the model
title_vectorizer = TfidfVectorizer(max_features=100, ngram_range=(1,2))

body_vectorizer = TfidfVectorizer(max_features=250, ngram_range=(1,2))
amenities_vectorizer = TfidfVectorizer(max_features=50, token_pattern=r'[^,]+')

# this is For 'title',
train_title_tfidf = title_vectorizer.fit_transform(train_data['title'])
test_title_tfidf = title_vectorizer.transform(test_data['title'])

# - thi is for 'body',
train_body_tfidf = body_vectorizer.fit_transform(train_data['body'])
test_body_tfidf = body_vectorizer.transform(test_data['body'])

# - and this is for the 'amenitiss'
train_amenities_tfidf = amenities_vectorizer.fit_transform(train_data['amenities'])
test_amenities_tfidf = amenities_vectorizer.transform(test_data['amenities'])

# -------------------------------
# 5. Preprocessor - it's tabular
# -------------------------------
# we are makin lists for which columns are numbers and which are category

number_features = [
    'bathrooms', 'bedrooms', 'square_feet', 'fee', 'has_photo',
    'amenities_count', 'latitude', 'longitude', 'hour_posted',
    'weekday_posted', 'is_weekend', 'title_len', 'body_len', 'amenities_len',
    'lat_bin', 'lon_bin'
]
category_features = [
    'category', 'pets_allowed', 'location', 'price_type', 'source'
]

# just making sure all number columns are numbers, not like words or weird stuff
for col in number_features:
    train_data[col] = pd.to_numeric(train_data[col], errors='coerce')
    test_data[col] = pd.to_numeric(test_data[col], errors='coerce')

# we're putting together a thing to clean up numbers and words before model, this part was also hard to write
preprocessor = ColumnTransformer([
    ('num', Pipeline([
        ('imputer', SimpleImputer(strategy='median')),  # fill in missing with middle value
        ('scaler', StandardScaler())  # make numbers more normal for our model
    ]), number_features),
    ('cat', Pipeline([
        ('imputer', SimpleImputer(strategy='most_frequent')),  # fill missing with most seen word
        ('ohe', OneHotEncoder(handle_unknown='ignore', sparse_output=True))  # turn words to 0 and 1s
    ]), category_features)
])

# -------------------------------------------
# 6. Preparing the Data
# -----------------------------------------
# we're gettin the prices (target) and features (inputs) ready
y = np.log1p(train_data['price'])  # make prices less crazy
X = train_data[number_features + category_features]
X_test = test_data[number_features + category_features]

# we are spliting the data for train and val, so we can see how good it does
# we know that most common picks are 0.20 , but we wanted a bit more data,
# for training and we used 0.15 for validation. And It seems it worked :)

X_train, X_val, y_train, y_val = train_test_split(X, y, test_size=0.15, random_state=42)

# we are doing all the cleaning and encoding on the data we just split
preprocessor.fit(X_train)
X_train_tab = preprocessor.transform(X_train)
X_val_tab = preprocessor.transform(X_val)
X_test_tab = preprocessor.transform(X_test)

# we put all features together, including the text stuff from before
X_train_final = hstack([X_train_tab,
                        train_title_tfidf[X_train.index],
                        train_body_tfidf[X_train.index],
                        train_amenities_tfidf[X_train.index]]).tocsr()
X_val_final = hstack([X_val_tab,
                      train_title_tfidf[X_val.index],
                      train_body_tfidf[X_val.index],
                      train_amenities_tfidf[X_val.index]]).tocsr()
X_test_final = hstack([X_test_tab,
                       test_title_tfidf,
                       test_body_tfidf,
                       test_amenities_tfidf]).tocsr()

# ------------------------------------------
# 7. Model Training (we chose LightGBM after some trials)
# ----------------------------------------------
# now we are making the model
# We use LGBMRegressor becausse after some research, we found out it's super fast and works good for tabular data,

lgbm = lgb.LGBMRegressor(
    boosting_type='gbdt',
    objective='regression',
    random_state=42,
    n_jobs=-1,
    n_estimators=1000
)

# We had tried lots of different settings to see which is best, and after getting the score around 170, we decided on that
# and also using google colab was not a good idea for us because executing and trying the changes took lots of time in each trial(1-2 hours)
param_dist = {
    'num_leaves': [15, 31, 50, 80],
    'learning_rate': [0.03, 0.05, 0.1, 0.15],
    'max_depth': [3, 5, 8, -1],
    'min_child_samples': [10, 20, 30],
    'subsample': [0.7, 0.8, 1.0],
    'colsample_bytree': [0.7, 0.9, 1.0],
    'reg_alpha': [0, 0.1, 1],
    'reg_lambda': [0, 0.1, 1]
}

# search for the best settings with cross validation
# RandomizedSearchCV tries random combos, We tried also gridsearch, but at the end randomized search worked better
# the n_iter is 20, n_splits is 3, we constantly changed these in order to find lowest error and fastest execution
# and we decide to use cross validation (splitting data a few times) to see which settings work the best
search = RandomizedSearchCV(
    lgbm, param_distributions=param_dist,
    n_iter=20, scoring='neg_mean_absolute_error',
    cv=KFold(n_splits=3, shuffle=True, random_state=42),
    verbose=1, n_jobs=-1, random_state=42
)
# we wanto to train the model with best settings we find
search.fit(X_train_final, y_train)

best_model = search.best_estimator_

# --------------------------------------
# 8. Validation Error
# -------------------------

# We are doing it to see how good it did on val data (not seen before our model)
val_pred = best_model.predict(X_val_final)
val_mae = mean_absolute_error(np.expm1(y_val), np.expm1(val_pred))
print(f"Validation MAE: {val_mae:.2f}")

# ----------------------------------------------------------------------------
# 9. Retraining On full data
# ----------------------------------------------------------------------------
# now we train on everything for best results
X_full = hstack([preprocessor.transform(X), train_title_tfidf, train_body_tfidf, train_amenities_tfidf]).tocsr()
best_model.fit(X_full, y)  # <-- no early_stopping_rounds, no eval_set

# --------------------------------------
# 10. Prediction and finally saving
# --------------------------------------
# we are planning to make the predictions for the test data and save to csv file
final_preds = np.expm1(best_model.predict(X_test_final))
results = pd.DataFrame({
    'Id': test_data['id'],
    'Predicted': final_preds
})
results.to_csv('housing_predictions.csv', index=False)

